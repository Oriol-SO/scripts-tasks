// Función para obtener todas las categorías activas
function obtenerCategoriasActivas($categorias) {
    return array_filter($categorias, function($categoria) {
        return $categoria['estado'] === 1;
    });
}

// Función para buscar categoría por ID
function buscarCategoriaPorId($categorias, $id) {
    foreach ($categorias as $categoria) {
        if ($categoria['id'] === $id) {
            return $categoria;
        }
    }
    return null;
}

// Función para buscar categoría por nombre
function buscarCategoriaPorNombre($categorias, $nombre) {
    foreach ($categorias as $categoria) {
        if (strtoupper($categoria['nombre']) === strtoupper($nombre)) {
            return $categoria;
        }
    }
    return null;
}

// Función para buscar categorías que contengan un texto
function buscarCategoriasPorTexto($categorias, $texto) {
    $resultado = [];
    $textoUpper = strtoupper($texto);
    foreach ($categorias as $categoria) {
        if (strpos(strtoupper($categoria['nombre']), $textoUpper) !== false) {
            $resultado[] = $categoria;
        }
    }
    return $resultado;
}

// Función para obtener lista de nombres de categorías
function obtenerNombresCategorias($categorias) {
    return array_map(function($categoria) {
        return $categoria['nombre'];
    }, $categorias);
}

// Función para obtener categorías por rango de IDs
function obtenerCategoriasPorRango($categorias, $idMin, $idMax) {
    return array_filter($categorias, function($categoria) use ($idMin, $idMax) {
        return $categoria['id'] >= $idMin && $categoria['id'] <= $idMax;
    });
}

// Función para agrupar categorías por tipo (basado en ID)
function agruparCategoriasPorTipo($categorias) {
    $grupos = [
        'principales' => [],      // ID 1-20
        'impuestos' => [],        // ID 26-41
        'equipamiento' => [],     // ID 56-93
        'servicios' => [],        // ID 132-143
        'especiales' => []        // ID 330+
    ];
    
    foreach ($categorias as $categoria) {
        $id = $categoria['id'];
        if ($id >= 1 && $id <= 20) {
            $grupos['principales'][] = $categoria;
        } elseif ($id >= 26 && $id <= 41) {
            $grupos['impuestos'][] = $categoria;
        } elseif ($id >= 56 && $id <= 93) {
            $grupos['equipamiento'][] = $categoria;
        } elseif ($id >= 132 && $id <= 143) {
            $grupos['servicios'][] = $categoria;
        } else {
            $grupos['especiales'][] = $categoria;
        }
    }
    
    return $grupos;
}

